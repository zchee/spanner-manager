func newSimpleSchema(tableNames ...string) *Schema {
	types := make([]Type, 0, len(tableNames))
	for _, tableName := range tableNames {
		typ := Type{
			Name:  snakeToCamel(tableName),
			Table: tableName,
			Fields: []Field{
				{
					Name:            "Id",
					ColumnName:      "id",
					GoType:          "int64",
					SpannerType:     "INT64",
					BaseSpannerType: "INT64",
					NotNull:         true,
					IsPrimaryKey:    true,
				},
			},
		}
		refreshTypeMetadata(&typ)
		types = append(types, typ)
	}
	return &Schema{Types: types}
}

