func TestGenerator_Generate_BasicFiles(t *testing.T) {
	typ := Type{
		Name:  "Users",
		Table: "Users",
		Fields: []Field{
			{
				Name:            "UserID",
				ColumnName:      "user_id",
				GoType:          "int64",
				SpannerType:     "INT64",
				BaseSpannerType: "INT64",
				NotNull:         true,
				IsPrimaryKey:    true,
			},
			{
				Name:            "Name",
				ColumnName:      "name",
				GoType:          "string",
				SpannerType:     "STRING(MAX)",
				BaseSpannerType: "STRING",
				NotNull:         true,
			},
		},
	}
	refreshTypeMetadata(&typ)

	outDir := generateFromSchema(t, &Schema{Types: []Type{typ}}, Options{
		PackageName: "models",
		Suffix:      ".yo.go",
	})
	if diff := cmp.Diff([]string{"spanner_db.yo.go", "users.yo.go"}, listFiles(t, outDir)); diff != "" {
		t.Fatalf("generated files mismatch (-want +got):\n%s", diff)
	}

	tests := map[string]struct {
		path     string
		contains []string
	}{
		"helper": {
			path: filepath.Join(outDir, "spanner_db.yo.go"),
			contains: []string{
				"Code generated by spanner-manager",
				"package models",
				"type SpannerDB interface",
				"var SpannerLog",
				"type JSONString[T any] struct",
			},
		},
		"type": {
			path: filepath.Join(outDir, "users.yo.go"),
			contains: []string{
				"Code generated by spanner-manager",
				"package models",
				"type Users struct",
				"UserID int64",
				`spanner:"name"`,
				"func UsersWritableColumns() []string",
				"func (t *Users) Insert() *spanner.Mutation",
				"func FindUsersByPrimaryKey(ctx context.Context, db SpannerDB, userID int64) (*Users, error)",
			},
		},
	}

	for name, tt := range tests {
		t.Run(name, func(t *testing.T) {
			content := readTextFile(t, tt.path)
			for _, want := range tt.contains {
				if !strings.Contains(content, want) {
					t.Fatalf("%s missing %q", tt.path, want)
				}
			}
		})
	}
}

