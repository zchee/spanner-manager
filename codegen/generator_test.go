func TestGenerator_Generate_TableSelection(t *testing.T) {
	tests := map[string]struct {
		opts      Options
		wantFiles []string
		wantErr   string
	}{
		"success: ignore tables": {
			opts: Options{
				PackageName:  "models",
				IgnoreTables: []string{"Secrets"},
			},
			wantFiles: []string{
				"spanner_db.spanner.go",
				"users.spanner.go",
			},
		},
		"success: include tables": {
			opts: Options{
				PackageName:   "models",
				IncludeTables: []string{"Secrets"},
			},
			wantFiles: []string{
				"secrets.spanner.go",
				"spanner_db.spanner.go",
			},
		},
		"error: unknown included table": {
			opts: Options{
				PackageName:   "models",
				IncludeTables: []string{"Missing"},
			},
			wantErr: `unknown table "Missing"`,
		},
	}

	for name, tt := range tests {
		t.Run(name, func(t *testing.T) {
			outDir := filepath.Join(t.TempDir(), "models")
			tt.opts.OutDir = outDir
			err := NewGenerator(tt.opts).Generate(newSimpleSchema("Users", "Secrets"))
			if tt.wantErr != "" {
				if err == nil {
					t.Fatalf("Generate() error = nil, want %q", tt.wantErr)
				}
				if !strings.Contains(err.Error(), tt.wantErr) {
					t.Fatalf("Generate() error = %v, want substring %q", err, tt.wantErr)
				}
				return
			}
			if err != nil {
				t.Fatalf("Generate() error = %v", err)
			}

			if diff := cmp.Diff(tt.wantFiles, listFiles(t, outDir)); diff != "" {
				t.Fatalf("generated files mismatch (-want +got):\n%s", diff)
			}
		})
	}
}

func TestGenerator_Generate_NamingOptions(t *testing.T) {
	tests := map[string]struct {
		schema          *Schema
		opts            Options
		wantFiles       []string
		wantTypeSnippet string
	}{
		"success: singularize with suffix": {
			schema: newSimpleSchema("FactoryControls"),
			opts: Options{
				PackageName:     "models",
				SingularizeRows: true,
				RowSuffix:       "Row",
			},
			wantFiles: []string{
				"factorycontrolrow.spanner.go",
				"spanner_db.spanner.go",
			},
			wantTypeSnippet: "type FactoryControlRow struct",
		},
		"success: row name override": {
			schema: newSimpleSchema("FleetPools"),
			opts: Options{
				PackageName: "models",
				Config: &Config{
					Tables: []TableConfig{
						{Name: "FleetPools", RowName: "FleetPoolRow"},
					},
				},
			},
			wantFiles: []string{
				"fleetpoolrow.spanner.go",
				"spanner_db.spanner.go",
			},
			wantTypeSnippet: "type FleetPoolRow struct",
		},
	}

	for name, tt := range tests {
		t.Run(name, func(t *testing.T) {
			outDir := filepath.Join(t.TempDir(), "models")
			tt.opts.OutDir = outDir

			if err := NewGenerator(tt.opts).Generate(tt.schema); err != nil {
				t.Fatalf("Generate() error = %v", err)
			}

			if diff := cmp.Diff(tt.wantFiles, listFiles(t, outDir)); diff != "" {
				t.Fatalf("generated files mismatch (-want +got):\n%s", diff)
			}

			typeFiles := filterFiles(tt.wantFiles, func(name string) bool {
				return strings.HasSuffix(name, ".spanner.go") && !strings.HasPrefix(name, "spanner_")
			})
			if len(typeFiles) != 1 {
				t.Fatalf("expected exactly one type file, got %v", typeFiles)
			}
			content := readTextFile(t, filepath.Join(outDir, typeFiles[0]))
			if !strings.Contains(content, tt.wantTypeSnippet) {
				t.Fatalf("%s missing %q", typeFiles[0], tt.wantTypeSnippet)
			}
		})
	}
}

